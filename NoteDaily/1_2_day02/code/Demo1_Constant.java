class Demo1_Constant {
	public static void main(String[] args) {
		System.out.println("Hello World!");
		/*
		* 字符串常量	用双引号括起来的内容
		* 整数常量		所有整数
		* 小数常量		所有小数
		* 字符常量		用单引号括起来的内容,里面只能放单个数字,单个字母或单个符号
		* 布尔常量		较为特殊，只有true和false
		* 空常量		    null(数组部分讲解)
		*/
		
		System.out.println("abc") ; //字符串常量
		System.out.println(1122); //整数常量
		System.out.println(14502.5);//  小数常量
		System.out.println('5');// 中间必须放单个单词,10代表二两字符,一个是1一个是0
		//System.out.println('');  //允许的
		//System.out.println(' ');   //可以放空格,表示空格字符
		System.out.println(true);
		System.out.println(false);
		
		
	}
}
